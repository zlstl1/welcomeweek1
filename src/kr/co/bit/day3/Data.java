package kr.co.bit.day3;

public class Data {
	String classno;
	String email;
	String kor;
	String eng;
	String math;
	String si;
	String hi;
	String total;
	String tea;
	String get;
	String local;
	public Data(String classno, String email, String kor, String eng, String math, String si, String hi, String total,
			String tea, String get, String local) {
		super();
		this.classno = classno;
		this.email = email;
		this.kor = kor;
		this.eng = eng;
		this.math = math;
		this.si = si;
		this.hi = hi;
		this.total = total;
		this.tea = tea;
		this.get = get;
		this.local = local;
	}
	public String getClassno() {
		return classno;
	}
	public void setClassno(String classno) {
		this.classno = classno;
	}
	public String getEmail() {
		return email;
	}
	public void setEmail(String email) {
		this.email = email;
	}
	public String getKor() {
		return kor;
	}
	public void setKor(String kor) {
		this.kor = kor;
	}
	public String getEng() {
		return eng;
	}
	public void setEng(String eng) {
		this.eng = eng;
	}
	public String getMath() {
		return math;
	}
	public void setMath(String math) {
		this.math = math;
	}
	public String getSi() {
		return si;
	}
	public void setSi(String si) {
		this.si = si;
	}
	public String getHi() {
		return hi;
	}
	public void setHi(String hi) {
		this.hi = hi;
	}
	public String getTotal() {
		return total;
	}
	public void setTotal(String total) {
		this.total = total;
	}
	public String getTea() {
		return tea;
	}
	public void setTea(String tea) {
		this.tea = tea;
	}
	public String getGet() {
		return get;
	}
	public void setGet(String get) {
		this.get = get;
	}
	public String getLocal() {
		return local;
	}
	public void setLocal(String local) {
		this.local = local;
	}
	
	
	
	
}
